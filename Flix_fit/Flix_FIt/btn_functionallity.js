//?File contains how button will interact 
function btn1() {
  //*Gets the element id and sets it to the varible
  let btn1 = document.getElementById("btn1");
  //*sets a prompt if the user wants to accpet or not
  if (confirm("Are you sure?")) {
    
    //*changes the style of the varible
    btn1.style.borderColor = "orange";
    btn1.style.backgroundColor = "orange";
    //*sets the documnet cookie to 1
    document.cookie = "btn1_select =1";
  } 
  else {
    //*sets the document cookie to 0
    document.cookie = "btn1_select=0";
    //*changes the style of the button
    btn1.style.borderColor = "lightgreen";
    btn1.style.backgroundColor = "lightgreen";
  }
}

